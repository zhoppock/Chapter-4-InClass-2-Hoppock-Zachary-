// Program with do a while loop on how many numbers the variable "I" is
// Version # 1
#include <iostream>
using namespace std;

int main()
{ 
  int I = 10;
  while (I < 20)
  {
    cout << "The value of I is " << I << endl;
    I++;
  }
  return 0;
}
